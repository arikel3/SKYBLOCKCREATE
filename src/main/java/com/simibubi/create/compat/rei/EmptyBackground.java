package com.simibubi.create.compat.rei;

public class EmptyBackground {

	private int width;
	private int height;

	public EmptyBackground(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}


	public int getHeight() {
		return height;
	}

}
